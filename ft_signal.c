#include "minishell.h"
#include <readline/readline.h>
#include <readline/history.h>
#include <stdlib.h>
#include <stdio.h>

void	ft_sig(int sig)
{
	if (sig == 2)
	{
		printf("\n");
		rl_on_new_line();
		rl_replace_line("", 0);
		rl_redisplay();
	}
	else if (sig == 3)
		printf("%s", "\n");
	else if (sig == 4)
	{
		printf("\nExit\n");
		exit (0);
	}
}
